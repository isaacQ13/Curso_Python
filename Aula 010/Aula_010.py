"""
Operadores Relacionais
==  -> Igualdade
 >  -> Maior que
 >= -> Maior ou igual
 <  -> Menor que
 <= -> Menor ou igual
 != -> Diferente de
"""
# Igualdade (==)
num_1 = 2
num_2 = 2

expressao = num_1 == num_2
print( expressao )   # Vai retornar em booleano

# Maior Que (>)

num_3 = 1
num_4 = 3

expressao_2 = num_3 > num_4
print(expressao_2)

# Maior ou igual (>=)

num_5 = 3
num_6 = 3

expressao_3 = num_5 >= num_6
print(expressao_3)

# Menor Que (<)

num_7 = 1
num_8 = 3

expressao_4 = num_7 < num_8
print(expressao_4)

# Menor ou igual (<=)

num_9 = 3
num_10 = 3

expressao_5 = num_9 <= num_10
print(expressao_5)

# Diferente de (!=)

num_11 = 1
num_12 = 3

expressao_6 = num_11 != num_12
print(expressao_6)